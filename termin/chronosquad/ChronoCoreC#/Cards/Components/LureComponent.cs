using System.Collections.Generic;

#if UNITY_64
using UnityEngine;
#endif
public class LureComponent : ItemComponent
{
    //float Radius = 2.0f;
    bool _lure_enabled = false;

    public LureComponent(ObjectOfTimeline owner) : base(owner) { }

    public override void Start()
    {
        Owner.MarkAsHero();
        (Owner.GetTimeline() as Timeline).InitHeroesArray();
    }

    public bool LureEnabled()
    {
        return _lure_enabled;
    }

    public void EnableLuring(bool en)
    {
        _lure_enabled = en;
    }

    public override ItemComponent Copy(ObjectOfTimeline owner)
    {
        return new LureComponent(owner);
    }

    public override void Execute(long timeline_step, long local_step) { }

    public void AddDisableLureEvent(long step)
    {
        Owner.AddCard(new ChangeLureStateEvent(step, false));
    }
}

public class ChangeLureStateEvent : EventCard<ObjectOfTimeline>
{
    bool en;

    public ChangeLureStateEvent(long step, bool en) : base(step)
    {
        this.en = en;
    }

    public ChangeLureStateEvent(long sstep, long fstep, bool en) : base(sstep, fstep)
    {
        this.en = en;
    }

    public override void on_forward_enter(long current_step, ObjectOfTimeline obj)
    {
        obj.GetComponent<LureComponent>().EnableLuring(en);
    }

    public override void on_backward_leave(long current_step, ObjectOfTimeline obj)
    {
        obj.GetComponent<LureComponent>().EnableLuring(!en);
    }

    //BEGIN################################################################
    // This code was generated by FieldScanner

    public override long HashCode()
    {
        long result = 0;
        result = FieldScanner.ModifyHash(result, en);
        result = FieldScanner.ModifyHash(result, start_step);
        result = FieldScanner.ModifyHash(result, finish_step);
        return result;
    }

    public override bool Equals(object obj)
    {
        if (obj == null)
            return false;
        if (obj.GetType() != GetType())
            return false;
        var other = obj as ChangeLureStateEvent;
        return en == other.en
            && start_step == other.start_step
            && finish_step == other.finish_step
            && true;
    }

    public override int GetHashCode()
    {
        return (int)HashCode();
    }
    //END################################################################
}
