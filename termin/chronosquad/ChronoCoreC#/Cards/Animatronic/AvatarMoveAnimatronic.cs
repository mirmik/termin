using System;
using System.Collections.Generic;

#if UNITY_64
using UnityEngine;
#endif

[Serializable]
class AvatarMoveAnimatronic : Animatronic
{
    ObjectId start_location;
    ObjectId finish_location;

    public AvatarMoveAnimatronic(
        ObjectId start_location,
        ObjectId finish_location,
        long start_step,
        long finish_step
    ) : base(start_step, finish_step)
    {
        this.start_location = start_location;
        this.finish_location = finish_location;
    }

    ReferencedPose FinalPose_(ITimeline tl)
    {
        return new ReferencedPose(new Pose(Vector3.zero, Quaternion.identity), finish_location);
    }

    ReferencedPose StartPose_(ITimeline tl)
    {
        if (start_location == default(ObjectId))
            return FinalPose(tl);
        return new ReferencedPose(new Pose(Vector3.zero, Quaternion.identity), start_location);
    }

    public override ReferencedPose GetReferencedPose(Int64 local_step, ITimeline tl)
    {
        var finish_object = tl.GetObject(finish_location);
        if (start_location == default(ObjectId))
            return FinalPose_(tl);

        if (local_step > FinishStep)
            return FinalPose_(tl);

        var start_object = tl.GetObject(start_location);
        if (local_step < StartStep)
            return StartPose_(tl);

        var start_pose = start_object.GlobalPose();
        var finish_pose = finish_object.GlobalPose();

        var LerpCoeff = (float)(local_step - StartStep) / (FinishStep - StartStep);

        var position = Vector3.Lerp(start_pose.position, finish_pose.position, LerpCoeff);
        var rotation = Quaternion.Slerp(start_pose.rotation, finish_pose.rotation, LerpCoeff);

        return ReferencedPose.FromGlobalPose(
            new Pose(position, rotation),
            FrameName(local_step),
            tl
        );
    }

    public ObjectId FrameName(Int64 stepstamp)
    {
        var center = (StartStep + FinishStep) / 2;
        return stepstamp < center ? start_location : finish_location;
    }

    public override AnimationType GetAnimationType(long step)
    {
        return AnimationType.Walk;
    }

    //BEGIN################################################################
