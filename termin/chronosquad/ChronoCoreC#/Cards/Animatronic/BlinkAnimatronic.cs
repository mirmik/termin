    public override AnimationType GetAnimationType(long local_step)
    {
        if (_is_croaching)
            return AnimationType.CroachIdle;
        else
            return AnimationType.Idle;
    }

    public override bool CanBeInterruptedForAction()
    {
        return false;
    }

