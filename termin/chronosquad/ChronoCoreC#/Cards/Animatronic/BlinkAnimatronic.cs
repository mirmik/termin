using System;
using System.Collections.Generic;

#if UNITY_64
using UnityEngine;
#endif

class BlinkedMovingState : Animatronic
{
    ReferencedPose _initial_pose;
    ReferencedPose _target_pose;
    bool _is_croaching = false;

    public BlinkedMovingState(
        ReferencedPose initial_pose,
        ReferencedPose target_pose,
        long start_step,
        long finish_step,
        bool is_croaching
    ) : base(start_step, finish_step)
    {
        _initial_pose = initial_pose;
        _target_pose = target_pose;
        _is_croaching = is_croaching;
    }

    public override bool IsCroach(ITimeline tl)
    {
        return _is_croaching;
    }

    public override ReferencedPose GetReferencedPose(Int64 stepstamp, ITimeline tl)
    {
        long center = (StartStep + FinishStep) / 2;
        if (stepstamp <= center)
        {
            return _initial_pose;
        }
        else
        {
            return _target_pose;
        }
    }

    public float Intensivity(Int64 stepstamp)
    {
        // lerp
        float center = (FinishStep - StartStep) / 2;
        float a = (stepstamp - StartStep) - center;
        return 1 - Math.Abs(a / center);
    }

