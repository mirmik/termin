public struct CubicCurve
{
    public Vector3 a;
    public Vector3 b;
    public Vector3 c;
    public Vector3 d;

    public CubicCurve(Vector3 f0, Vector3 f1, Vector3 d0, Vector3 d1)
    {
        a = 2 * f0 - 2 * f1 + d0 + d1;
        b = -3 * f0 + 3 * f1 - 2 * d0 - d1;
        c = d0;
        d = f0;
    }

    public Vector3 Evaluate(float t)
    {
        var t2 = t * t;
        var t3 = t2 * t;
        return a * t3 + b * t2 + c * t + d;
    }

    public Vector3 EvaluateDerivative(float t)
    {
        var t2 = t * t;
        return 3 * a * t2 + 2 * b * t + c;
    }

    public bool Equals(CubicCurve other)
    {
        return a == other.a && b == other.b && c == other.c && d == other.d;
    }

    public override bool Equals(object obj)
    {
        if (obj == null)
            return false;
        if (obj.GetType() != GetType())
            return false;
        return Equals((CubicCurve)obj);
    }

    public static bool operator ==(CubicCurve a, CubicCurve b)
    {
        return a.Equals(b);
    }

    public static bool operator !=(CubicCurve a, CubicCurve b)
    {
        return !a.Equals(b);
    }

    public long HashCode()
    {
        long result = 0;
        result = FieldScanner.ModifyHash(result, a);
        result = FieldScanner.ModifyHash(result, b);
        result = FieldScanner.ModifyHash(result, c);
        result = FieldScanner.ModifyHash(result, d);
        return result;
    }

    public override int GetHashCode()
    {
        return (int)HashCode();
    }
}

public class CubicMoveAnimatronic : Animatronic
{
    public const float F = 20.0f;
    ReferencedPose pose_a;
    ReferencedPose pose_b;

    CubicCurve curve;

    public CubicMoveAnimatronic(
        ReferencedPose pose_a,
        ReferencedPose pose_b,
        long start_step,
        long finish_step
    ) : base(start_step, finish_step)
    {
        this.pose_a = pose_a;
        this.pose_b = pose_b;

        var lpose_a = pose_a.LocalPose();
        var lpose_b = pose_b.LocalPose();

        var dir_a = lpose_a.Forward() * F;
        var dir_b = lpose_b.Forward() * F;
        var pnt_a = lpose_a.position;
        var pnt_b = lpose_b.position;

        curve = new CubicCurve(pnt_a, pnt_b, dir_a, dir_b);
    }

    public override ReferencedPose GetReferencedPose(long stepstamp, ITimeline tl)
    {
        float t = (float)(stepstamp - start_step) / (finish_step - start_step);

        var x = curve.Evaluate(t);
        var dx = curve.EvaluateDerivative(t);

        var pose = new Pose(x, Quaternion.LookRotation(dx));
        return new ReferencedPose(pose, pose_a.Frame);
    }

    public override AnimationType GetAnimationType(long local_step)
    {
        return AnimationType.None;
    }

    //BEGIN################################################################
    // This code was generated by FieldScanner

    public override long HashCode()
    {
        long result = 0;
        result = FieldScanner.ModifyHash(result, pose_a);
        result = FieldScanner.ModifyHash(result, pose_b);
        result = FieldScanner.ModifyHash(result, curve);
        result = FieldScanner.ModifyHash(result, _initial_animation_time);
        result = FieldScanner.ModifyHash(result, unchanged_start_step);
        result = FieldScanner.ModifyHash(result, unchanged_finish_step);
        result = FieldScanner.ModifyHash(result, start_step);
        result = FieldScanner.ModifyHash(result, finish_step);
        return result;
    }

    public override bool Equals(object obj)
    {
        if (obj == null)
            return false;
        if (obj.GetType() != GetType())
            return false;
        var other = obj as CubicMoveAnimatronic;
        return pose_a == other.pose_a
            && pose_b == other.pose_b
            && curve == other.curve
            && _initial_animation_time == other._initial_animation_time
            && unchanged_start_step == other.unchanged_start_step
            && unchanged_finish_step == other.unchanged_finish_step
            && start_step == other.start_step
            && finish_step == other.finish_step
            && true;
    }

    public override int GetHashCode()
    {
        return (int)HashCode();
    }
    //END################################################################
}
