    public ShootAnimatronic(
        long start_step,
        long finish_step,
        ReferencedPose pose,
        Vector3 direction,
        bool croach = false
    ) : base(start_step, finish_step)
    {
        _initial_pose = pose;
        _direction = new Vector3(direction.x, direction.y, direction.z).normalized;
        _direction.y = 0;
        this.croach = croach;
    }

