public class SlugSnipeKillaCommand : ShootCommand
{
    public string slug_name;

    public override void KillTarget(ObjectOfTimeline actor, ITimeline tl, float absolute_offset)
    {
        var slug = tl.GetObject(slug_name);
        var target = TargetObject(tl);

        var jump_anim = new PoseLerpAnimatronic(
            start_step: tl.CurrentStep(),
            finish_step: tl.CurrentStep() + (long)(Utility.GAME_GLOBAL_FREQUENCY * 0.5f),
            pose_a: actor.CurrentReferencedPose(),
            pose_b: target.CurrentReferencedPose()
        );
        slug.SetNextAnimatronic(jump_anim);

        Debug.Log("SlugSnipeKillaCommand.KillTarget");
        tl.AddEvent(
            new DeathEvent(
                step: tl.CurrentStep()
                    + (long)(0 * actor.ReverseMultiplier() * Utility.GAME_GLOBAL_FREQUENCY),
                actor: target,
                who_kill_me: actor.Name(),
                reversed: target.IsReversed()
            )
        );

        var set_hosted = new SetHostedEvent(slug.LocalStep(), next: default, prev: slug._hosted);
        slug.AddCard(set_hosted);
    }

    public SlugSnipeKillaCommand(
        ObjectId target_actor,
        float shoot_distance,
        long stamp,
        string slug_name
    ) : base(target_actor, shoot_distance, stamp, false, can_shoot_from_croach: true)
    {
        this.slug_name = slug_name;
    }

