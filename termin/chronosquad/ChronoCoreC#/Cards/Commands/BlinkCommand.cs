using System.Collections.Generic;
using System;

#if UNITY_64
using UnityEngine;
#endif

[Serializable]
public class BlinkCommand : ActorCommand
{
    private ReferencedPoint position;
    float BlinkTimeLapse = 0.5f;

    public BlinkCommand(ReferencedPoint position, float BlinkTimeLapse, long stamp) : base(stamp)
    {
        this.position = position;
        this.BlinkTimeLapse = BlinkTimeLapse;
    }

    public override bool Execute(ObjectOfTimeline actor, ITimeline timeline)
    {
        var chronosphere = timeline.GetChronosphere();
        var selected_object_position = actor.Position();
        var target_position = GameCore.FindNearestPointOnNavMesh(position.GlobalPosition(timeline));

        ObjectOfTimeline guard = actor;

        var local_step = guard.LocalStep();
        var global_step = timeline.CurrentStep();

        var target_direction = TargetDirection(guard, target_position, timeline);
        var target_global_pose = new Pose(target_position, target_direction);

        guard.SetNextAnimatronic(
            new BlinkedMovingState(
                initial_pose: guard.CurrentReferencedPose(),
                target_pose: ReferencedPose.FromGlobalPose(
                    target_global_pose,
                    position.Frame,
                    timeline
                ),
                start_step: local_step + 1,
                finish_step: local_step
                    + 2
                    + (Int64)(BlinkTimeLapse * Utility.GAME_GLOBAL_FREQUENCY),
                is_croaching: (actor as Actor).IsCroach()
            )
        );

        timeline.AddEvent(
            new BlinkEffectEvent(
                start_step: global_step + 1,
                finish_step: global_step
                    + 2
                    + (Int64)(BlinkTimeLapse * Utility.GAME_GLOBAL_FREQUENCY),
                position1: guard.Position(),
                position2: target_position //,
            //BlinkEffectMaterial: BlinkEffectMaterial
            )
        );

        return true;
    }

    public ReferencedPoint TargetPosition()
    {
        return position;
    }

    Vector3 TargetDirection(ObjectOfTimeline guard, Vector3 target_position, ITimeline tl)
    {
        var simple_direction = target_position - guard.Position();
        var closest = Utility.GetClosestActor(
            target_position,
            tl,
            new MyList<ObjectOfTimeline>() { guard }
        );
        if (closest == null)
        {
            simple_direction.y = 0;
            simple_direction.Normalize();
            return simple_direction;
        }

        var distance = Vector3.Distance(target_position, closest.position());
        if (distance < 10)
        {
            var direction = closest.position() - target_position;
            direction.y = 0;
            direction.Normalize();
            return direction;
        }
        else
        {
            simple_direction.y = 0;
            simple_direction.Normalize();
            return simple_direction;
        }
    }

    //BEGIN################################################################
