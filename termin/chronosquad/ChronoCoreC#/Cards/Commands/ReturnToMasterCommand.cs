using System;
using UnityEngine;

public class ReturnToMasterCommand : ClosestCommand
{
    public ReturnToMasterCommand(ObjectId target_actor, long stamp)
        : base(target_actor, WalkingType.Run, 0.1f, stamp) { }

    public override bool ActionPhase(ObjectOfTimeline actor, ITimeline timeline)
    {
        var set_hosted_event = new SetHostedEvent(
            step: actor.LocalStep(),
            next: TargetActorName(),
            prev: actor._hosted
        );
        actor.AddCard(set_hosted_event);

        return true;
    }

    public override bool IsActionStarted(ObjectOfTimeline actor)
    {
        return false;
    }
}

public class SetHostedEvent : EventCard<ObjectOfTimeline>
{
    ObjectId _next_hosted;
    ObjectId _prev_hosted;

    public SetHostedEvent(long step, ObjectId next, ObjectId prev) : base(step)
    {
        _next_hosted = next;
        _prev_hosted = prev;
    }

    public override void on_forward_enter(long step, ObjectOfTimeline actor)
    {
        actor.SetHosted(_next_hosted);

        if (actor.GetTimeline().GetChronosphere().SelectedObjectName() == actor.ObjectId())
        {
            actor
                .GetTimeline()
                .GetChronosphere()
                .Select(actor.GetTimeline().GetObject(_next_hosted));
        }
    }

    public override void on_backward_leave(long step, ObjectOfTimeline actor)
    {
        actor.SetHosted(_prev_hosted);
    }

    //BEGIN################################################################
    // This code was generated by FieldScanner

    public override long HashCode()
    {
        long result = 0;
        result = FieldScanner.ModifyHash(result, _next_hosted);
        result = FieldScanner.ModifyHash(result, _prev_hosted);
        result = FieldScanner.ModifyHash(result, start_step);
        result = FieldScanner.ModifyHash(result, finish_step);
        return result;
    }

    public override bool Equals(object obj)
    {
        if (obj == null)
            return false;
        if (obj.GetType() != GetType())
            return false;
        var other = obj as SetHostedEvent;
        return _next_hosted == other._next_hosted
            && _prev_hosted == other._prev_hosted
            && start_step == other.start_step
            && finish_step == other.finish_step
            && true;
    }

    public override int GetHashCode()
    {
        return (int)HashCode();
    }
    //END################################################################
}
