#if UNITY_64
using UnityEngine;
#endif

using System;
using System.Collections.Generic;

[Serializable]
public class LoudSoundEvent : GlobalEvent
{
    ReferencedPoint pos;
    float radius;
    RestlessnessParameters noise_parameters;

    public LoudSoundEvent(
        long step,
        ReferencedPoint center,
        float radius,
        RestlessnessParameters noise_parameters
    ) : base(step + 1, step + 2)
    {
        this.pos = center;
        this.radius = radius;
        this.noise_parameters = noise_parameters;
    }

    public MyList<ObjectOfTimeline> ListOfEnemiesInRadius(ITimeline tl)
    {
        MyList<ObjectOfTimeline> result = new MyList<ObjectOfTimeline>();
        var enemies = tl.Enemies();
        foreach (var enemy in enemies)
        {
            var enemy_global_position = enemy.GlobalPosition();
            if (Vector3.Distance(enemy_global_position, pos.GlobalPosition(tl)) < radius)
            {
                result.Add(enemy);
            }
        }
        Debug.Log("ListOfEnemiesInRadius: " + result.Count);
        return result;
    }

    public override void on_forward_enter(long current_step, ITimeline tl)
    {
        var enemies = ListOfEnemiesInRadius(tl);
        foreach (var enemy in enemies)
        {
            enemy.HearNoise(current_step, pos, noise_parameters);
        }
    }

    public override void on_backward_leave(long current_step, ITimeline tl) { }

    //BEGIN################################################################
    // This code was generated by FieldScanner

    public override long HashCode()
    {
        long result = 0;
        result = FieldScanner.ModifyHash(result, pos);
        result = FieldScanner.ModifyHash(result, radius);
        result = FieldScanner.ModifyHash(result, noise_parameters);
        result = FieldScanner.ModifyHash(result, start_step);
        result = FieldScanner.ModifyHash(result, finish_step);
        return result;
    }

    public override bool Equals(object obj)
    {
        if (obj == null)
            return false;
        if (obj.GetType() != GetType())
            return false;
        var other = obj as LoudSoundEvent;
        return pos == other.pos
            && radius == other.radius
            && noise_parameters == other.noise_parameters
            && start_step == other.start_step
            && finish_step == other.finish_step
            && true;
    }

    public override int GetHashCode()
    {
        return (int)HashCode();
    }
    //END################################################################
}
