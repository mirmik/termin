#if UNITY_64
using UnityEngine;
#endif

using System;

class LoudSoundVisualEffect : GlobalEvent
{
    private ReferencedPoint position;
    float maxRadius;

    [IgnoreRefflectionAttribute]
    LoudWave waveEffect = null;

    public LoudSoundVisualEffect(
        long start_step,
        long finish_step,
        ReferencedPoint position,
        float maxRadius
    ) : base(start_step, finish_step)
    {
        this.maxRadius = maxRadius;
        this.position = position;
    }

    public override void on_enter(long current_step, ITimeline tl)
    {
        if (tl.IsTimeSpirit)
            return;

        if (waveEffect == null)
        {
            var waveEffectgp = GameObject.Instantiate(
                MaterialKeeper.Instance.GetPrefab("LoudWaveEffect")
            );
            waveEffect = waveEffectgp.GetComponent<LoudWave>();
        }
        waveEffect.transform.position = position.GlobalPosition(tl);
        waveEffect.gameObject.layer = 13;
        waveEffect.SetMinRadius(0);
        waveEffect.SetMaxRadius(maxRadius);
    }

    public override void on_leave(long current_step, ITimeline tl)
    {
        if (tl.IsTimeSpirit)
            return;

        GameObject.Destroy(waveEffect.gameObject);
        waveEffect = null;
    }

    public override void update(long current_step, ITimeline tl)
    {
        if (tl.IsTimeSpirit)
            return;

        waveEffect.transform.position = position.GlobalPosition(tl);
        float phase = (current_step - StartStep) / (float)(FinishStep - StartStep);
        waveEffect.SetPhase(phase);
    }

    //BEGIN################################################################
    // This code was generated by FieldScanner

    public override long HashCode()
    {
        long result = 0;
        result = FieldScanner.ModifyHash(result, position);
        result = FieldScanner.ModifyHash(result, maxRadius);
        result = FieldScanner.ModifyHash(result, start_step);
        result = FieldScanner.ModifyHash(result, finish_step);
        return result;
    }

    public override bool Equals(object obj)
    {
        if (obj == null)
            return false;
        if (obj.GetType() != GetType())
            return false;
        var other = obj as LoudSoundVisualEffect;
        return position == other.position
            && maxRadius == other.maxRadius
            && start_step == other.start_step
            && finish_step == other.finish_step
            && true;
    }

    public override int GetHashCode()
    {
        return (int)HashCode();
    }
    //END################################################################
}
